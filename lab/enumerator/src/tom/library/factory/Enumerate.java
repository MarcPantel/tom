package tom.library.factory;

import java.lang.annotation.*;

@Target({ElementType.CONSTRUCTOR, ElementType.METHOD, ElementType.PARAMETER})
@Retention(RetentionPolicy.RUNTIME)
public @interface Enumerate {
    int maxSize() default 8; // maybe should be infinite (use -1)
    boolean canBeNull() default false; // should be always false if ElementType.METHOD (corresponding to this)
    boolean singleton() default false; // only one element of the enumeration generated by Constructors is used to represent "this"
    int singletonSize() default 0; // size of the element to use as singleton
}

